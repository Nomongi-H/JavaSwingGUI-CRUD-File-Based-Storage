//Login Button
    private void btnLoginUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginUserActionPerformed
      // Get the text that the user entered into the username text field
        String username = txtLoginUser.getText();
        // Get the password from the password field and convert it to a String
        // (Password fields return char[] for security reasons, so we convert it)
        String password = new String(txtPasswordLogin.getPassword());

        // Check if the entered username and password are correct using the user_storage class
        if (user_storage.authenticate(username, password)) {
             // If authentication is successful, show a success message to the user
            JOptionPane.showMessageDialog(this, "Login successful!");
            // Switch to Manage Users tab after login
            // Here, tab index 2 represents the Manage Users section in the tabbed pane
            jTabbedPane2.setSelectedIndex(2);   // assuming tab 2 is Manage Users
        } else {
             // If authentication fails, show an error message
            JOptionPane.showMessageDialog(this, "Invalid username or password.");
        }
    }//GEN-LAST:event_btnLoginUserActionPerformed
//Reg Button
    private void btnRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegActionPerformed
        // Get the text that the user entered into the registration username field
        String username = txtUsernameReg.getText();
        // Get the password entered into the registration password field and convert it to a String
        String password = new String(txtPasswordReg.getPassword());

        //Validation step
        // Check if either the username or password fields are empty
        if (username.isEmpty() || password.isEmpty()) {
            // If either field is empty, show a message to the user
            JOptionPane.showMessageDialog(this, "Username and password required.");
            // Stop the registration process here (exit the method)
            return;
        }
        //// Hash the user's password for secure storage using SHA-256
        String hashed = user_storage.hashPassword(password);
        // Create a new User object with the provided username, hashed password,
        // an empty email (""), and a default role of "User"
        User newUser = new User(username, hashed, " ", "User");  // default role

        // Attempt to add the new user to the storage system (e.g., users.txt file)
        if (user_storage.addUser(newUser)) {
            // If adding was successful, inform the user that registration worked
            JOptionPane.showMessageDialog(this, "Registration successful! You may now log in.");
            // Clear the login fields so the user can log in with the new account
            txtLoginUser.setText("");
            txtPasswordLogin.setText("");
            // Switch to the Login tab (assuming tab index 1 is the Login screen)
            jTabbedPane2.setSelectedIndex(1);
        } else {
            // If adding failed, it usually means the username already exists
            JOptionPane.showMessageDialog(this, "Username already exists.");
        }
    }//GEN-LAST:event_btnRegActionPerformed

