    // This method updates (refreshes) the user table on the interface 
    // to display the most recent list of users from storage
    private void refreshUserTable() {
        // Read all users from the storage file (e.g., users.txt) and store them in a list
        List<User> users = user_storage.readAll();
        // Get the table model of the JTable (tblUsers), which controls the data shown in the table
        DefaultTableModel model = (DefaultTableModel) tblUsers.getModel();
        // Clear all existing rows in the table before adding updated data
        model.setRowCount(0);
        // Loop through each user in the list and add their details as a new row in the table
        users.forEach(u -> model.addRow(new Object[]{
                u.getUsername(), // Column 1: Username
                u.getEmail(), // Column 2: Email
                u.getRole() // Column 3: Role (e.g., "Admin" or "User")
        }));
    }

