// Add user button
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
         String username = txtUsernameManage.getText();
            String email = txtEmailManage.getText();
            String role = txtRoleManage.getText();
            String password = new String(txtPasswordManage.getPassword());

            if (username.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Username & Password required.");
                return;
            }

            User u = new User(username, user_storage.hashPassword(password), email, role);
            if (user_storage.addUser(u)) {
                JOptionPane.showMessageDialog(this, "User added.");
                refreshUserTable();
            } else {
                JOptionPane.showMessageDialog(this, "Username exists.");
            }  
    }//GEN-LAST:event_btnAddActionPerformed
//Update user button
